
public class OperadoresIncrementoDecremento {
	public static void main(String[] args) {
		
		int a , b;
		
		a = 10;
		
		b = a++; //Priemero coloca o valor de A em B e depois incrementa A
		
		b = ++a; //Priemeiro incrementa A e depois adicona valor de A em B;
		
		System.out.println("A: " + a);
		
		System.out.println("B: " + b);
		
	}
}
